    void setComboBox(){

        if(SQLite.openDB()){
            String[][] laptop = SQLite.read("product"," ProductType='Laptop'");
            String[][] ram = SQLite.read("product"," ProductType='RAM'");
            String[][] acce = SQLite.read("product"," ProductType='Accessories'");
            String[][] sd = SQLite.read("product"," ProductType='SD Card'");
            String[][] other = SQLite.read("product"," ProductType='Other'");
            
           int totalProduct = (laptop.length+1) + (ram.length+1)+ (acce.length+1)+ (sd.length+1)
           + (other.length+1)+1 ;
           String[] products = new String[totalProduct];
            int product_index = 0;
            products[product_index] = "";
            product_index++;
            if(laptop.length > 0){
               
                products[product_index] = "----- Laptop -----";  
                product_index++;
                for(int j=0;j<laptop.length;j++){
                    products[product_index] = laptop[j][2];
                    product_index++; 
                }            
                }
               if(ram.length > 0){
                products[product_index] = "----- RAM -----";  
                product_index++;
                for(int j=0;j<ram.length;j++){
                    products[product_index] = ram[j][2];
                    product_index++;
                }  
                }
                if(acce.length > 0){
                products[product_index] = "----- Accessories -----";  
                product_index++;
                for(int j=0;j<acce.length;j++){
                    products[product_index] = acce[j][2];
                    product_index++;
                }
                }
                if(sd.length > 0){
                products[product_index] = "----- SD Card -----";  
                product_index++;
                for(int j=0;j<sd.length;j++){
                    products[product_index] = sd[j][2];
                    product_index++;
                }
                 }
                  if(other.length > 0){
                products[product_index] = "----- Others -----";  
                product_index++;
                for(int j=0;j<other.length;j++){
                    products[product_index] = other[j][2];
                    product_index++;
                }
                }
               
                
                 
                  
                        
            
            
//            products[1] = records[0][2];
//            products[2] = records[1][2];
            
            cmb_product.setModel(new javax.swing.DefaultComboBoxModel(products));
            SQLite.closeDB();
            
            
        }
        
    }
    
    
    

