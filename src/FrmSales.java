    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
     
        String datetime = txt_id.getText();
        String cashiersName = C_Name.getText();
        String customersName =  Cus_Name.getText();
        String productName = this.cmb_product.getSelectedItem().toString();
        String quantity = Qty.getText();
        
        String price = txt_price.getText();
        
        double total = Double.parseDouble(price)*Double.parseDouble(quantity);
        
     
       
            if(SQLite.openDB()){
                
                String[][] product = SQLite.read("product", " ProductName='"+productName+"'");
                int remainingQty = Integer.parseInt(product[0][3]);
                int orderQty = Integer.parseInt(quantity);
                
                
                if(remainingQty < orderQty){
                    JOptionPane.showMessageDialog(null, "Out of Stock!");
                }
                
                
                else{
                
                    int autoincrement = SQLite.autoincrement("Sales")+1;
                    String values =  "'" +autoincrement + "'"+ "," +"'" +datetime + "'"+ "," + "'" +cashiersName + 
                    "'"  + ","+"'" +customersName+"'"+ " , " + "'" + productName +  "'" + "," + "'" + quantity + "'"+","+ "'" + total + "'";
                   if (SQLite.create("Sales",values)){
                       javax.swing.JOptionPane.showMessageDialog(null, "Sucessfully saved!");
                       
                       // Update Product Quantity
                       int updateQty = remainingQty - orderQty;
                       if(SQLite.update("product", " QTY="+updateQty , " ProductName='"+productName+"'")){
                            javax.swing.JOptionPane.showMessageDialog(null, "Success!");
                       }
                       else{
                            javax.swing.JOptionPane.showMessageDialog(null, "Fields must not left blank");
                       }
                  
                }
                }
                
        
           SQLite.closeDB();
             
    }
        
         
    }//GEN-LAST:event_jButton1ActionPerformed

